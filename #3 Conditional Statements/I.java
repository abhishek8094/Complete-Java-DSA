//Switch Case

public class I{
    public static void main(String args[]){
        int number=2;

        switch(number){
            case 1:System.out.println("Samosa");
                 break;
            case 2:System.out.println("Faluda");
                 break;
            case 3:System.out.println("Momos");
                 break;
            case 4:System.out.println("Burger");
                 break;
            default:System.out.println("We wake up");

        }
    }
}